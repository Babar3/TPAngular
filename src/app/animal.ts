export class Animal {

    name: string;
    species: string;
    veterinarian: string;
    comment: string;
    email: string;
    phoneNumber: string;

    constructor(name, species, veterinarian, comment, email, phoneNumber) {
        this.name = name;
        this.species = species;
        this.veterinarian = veterinarian;
        this.comment = comment;
        this.email = email;
        this.phoneNumber = phoneNumber;
    }

}
